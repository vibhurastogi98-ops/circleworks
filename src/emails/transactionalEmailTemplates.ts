export type TransactionalEmailTemplateSlug =
  (typeof transactionalEmailTemplates)[number]["slug"];

export function getTransactionalEmailTemplate(
  slug: TransactionalEmailTemplateSlug
) {
  const template = transactionalEmailTemplates.find((item) => item.slug === slug);

  if (!template) {
    throw new Error(`Unknown transactional email template: ${slug}`);
  }

  return template;
}
